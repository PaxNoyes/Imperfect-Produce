// Navigates to Google and searches for Bacon
Cypress.Commands.add("googleBacon", () => {
  cy.visit("http://google.com");
  cy.get('.gLFyf').type("bacon {enter}");
  cy.get('#rcnt').contains("Bacon - Wikipedia");
});